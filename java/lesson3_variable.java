package learningcoding;

import java.util.Scanner;

public class lesson3varible {
	public static void main(String[] args) {
		Scanner myCar = new Scanner(System.in);
		// assigned a variable manually;
		/*
		 * Read a byte - nextByte() Read a short - nextShort() Read an int - nextInt()
		 * Read a long - nextLong() Read a float - nextFloat() Read a double -
		 * nextDouble() Read a boolean - nextBoolean() Read a complete line - nextLine()
		 * Read a word - next()
		 */
		System.out.println(myCar.nextInt());
	}
}
